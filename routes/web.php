Route::get('/', function () {
    return view('welcome');
});

Route::middleware([
    'auth:sanctum',
    config('jetstream.auth_session'),
    'verified'
])->group(function () {
    Route::get('/dashboard', function () {
        //Passing the latest sorting option chosen to the dashboard so the selected sorting session variable is never empty even on first connection
        session(['selected_sorting' => 'asc']);
        return view('dashboard');
    })->name('dashboard');

    //Added a sort parameter to the index route
    Route::get('tasks/{sort}', [TaskController::class, 'index'])->name('tasks.index');
    Route::post('tasks', [TaskController::class, 'store'])->name('tasks.store');
    Route::put('tasks/{task}', [TaskController::class, 'update'])->name('tasks.update');
    Route::delete('tasks/{task}', [TaskController::class, 'destroy'])->name('tasks.destroy');
});
